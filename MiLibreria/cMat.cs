using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace MiLibreria
{
    class cMat
    {
        public double suma(double pA, double pB)
        {
            return pA + pB;
        }

        public double resta(double pA, double pB)
        {
            return pA - pB;
        }

        public double multi(double pA, double pB)
        {
            return pA * pB;
        }

        public double div(double pA, double pB)
        {
            return pA / pB;
        }
    }

}
